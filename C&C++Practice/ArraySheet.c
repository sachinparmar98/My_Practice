#include <stdio.h>
void main()
{
	int n;
	printf("ENTER ARRAY CAPACITY\n");
	scanf("%d",&n);
	
	int x[n];
	for(int i=0;i<n;i++)
	{
		printf("enter value\n");
		scanf("%d",&x[i]);
	}
	int largest=x[0],smallest=x[0];
	for(int i=1;i<sizeof(x)/sizeof(x[0]);i++)
	{
		if(smallest>x[i])smallest=x[i];
		else if(largest<x[i])largest=x[i];
	}
	printf("smallest element=%d\n",smallest);
	printf("largest element=%d\n",largest);
}
